package com.initech.ini.maven.util;

@SuppressWarnings("serial")
public class IniBuildException extends Exception {

	public IniBuildException() {
		super();
	}

	public IniBuildException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public IniBuildException(String arg0) {
		super(arg0);
	}
	
	public IniBuildException(Throwable arg0) {
		super(arg0);
	}
	
}
